#  1. INTEGER n
#  2. INTEGER_ARRAY ar
#

def sockMerchant(n, ar):
    sock_colorcount = [0]*101
    total_pairs = 0
    
    for sock_number in ar:
        sock_colorcount[sock_number] += 1

    for color_count in sock_colorcount:
        total_pairs += int(color_count/2)
            
    return total_pairs
            
if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    n = int(input().strip())

    ar = list(map(int, input().rstrip().split()))

    result = sockMerchant(n, ar)

    fptr.write(str(result) + '\n')

    fptr.close()
